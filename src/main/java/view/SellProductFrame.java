    private void sellTableButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sellTableButtonActionPerformed
        if(sellTableAmount.getText().equals("")){
            messageSellTable.setText("Please enter an amount to sell WoodenTable");
        }else{
            int sellAmount=Integer.parseInt(sellTableAmount.getText());
            String message=CustomerOperations.sellWoodenTable(customer, sellAmount);
            messageSellTable.setText(message);
            fillProductAmountInformation();
        }
    }//GEN-LAST:event_sellTableButtonActionPerformed

